    public function fetchCusCreditnotes(Request $request){

        $query = CustomerCdNote::query();

        if ($request->has('id') && $request->id != '') {
            $query->where('id', '=', '' . $request->id . '');
        }
        if ($request->has('from_date') && $request->from_date != '') {
            $query->where('date', '>=', '' . $request->from_date . '');
        }
        if ($request->has('to_date') && $request->to_date != '') {
            $query->where('date', '<=', '' . $request->to_date . '');
        }
        if ($request->has('customer_id') && $request->customer_id != '') {
            $query->where('customer_id', '=', '' . $request->customer_id . '');
        }

        $query->where('status', 1);
        $query->orderBy('id', 'asc'); // Default ordering

        $fetchTableDetails = $query->get();
        //$fetchTableDetails = Currencies::all();

        $responses = '';

        $debit_total = 0;
        $credit_total = 0;

        $fetchTableDetails = $query->paginate(100);

        //$parentRoute = 'index.productcategories';

        $responses = view('pages.dashboard.customers.tables.credit_notes_table', compact('fetchTableDetails'))->render();

        return response()->json(['html' => $responses]);

